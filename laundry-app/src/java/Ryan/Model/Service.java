public class Service {

    private int serviceId;
    private String layanan;
    private String jenisCucian;
    private double price;

    public int getServiceId() { return serviceId; }
    public void setServiceId(int serviceId) { this.serviceId = serviceId; }

    public String getLayanan() { return layanan; }
    public void setLayanan(String layanan) { this.layanan = layanan; }

    public String getJenisCucian() { return jenisCucian; }
    public void setJenisCucian(String jenisCucian) { this.jenisCucian = jenisCucian; }

    public double getPrice() { return price; }
    public void setPrice(double price) { this.price = price; }
}

